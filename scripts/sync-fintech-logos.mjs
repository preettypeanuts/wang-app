import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";

const CDN_VERSION = "2.5.0";
const MANIFEST_URL = `https://cdn.jsdelivr.net/npm/idn-finlogos@${CDN_VERSION}/dist/manifest.json`;
const CDN_BASE = `https://cdn.jsdelivr.net/npm/idn-finlogos@${CDN_VERSION}/dist/icons`;
const OUTPUT_DIR = path.join(process.cwd(), "public/icons/fintech");
const CATALOG_OUTPUT = path.join(
  process.cwd(),
  "config/wallet-institution-catalog.generated.ts",
);
const BRAND_COLORS_OUTPUT = path.join(
  process.cwd(),
  "config/wallet-institution-brand-colors.generated.ts",
);

const SYNC_CATEGORIES = new Set(["bank-logo", "e-wallet", "remittance"]);
const EXTRA_SLUGS = new Set([
  "alipay",
  "apple-pay",
  "byond-bsi",
  "google-pay",
  "grab-pay",
  "samsung-pay",
  "wechat-pay",
]);

const SKIP_SLUG_PATTERN = /-(?:alt|old)(?:-alt)?$/;
const SKIP_FILLS = new Set([
  "none",
  "#fff",
  "#ffffff",
  "#fefefe",
  "#000",
  "#000000",
]);

function isVariantSlug(slug) {
  return SKIP_SLUG_PATTERN.test(slug);
}

function normalizeHex(hex) {
  const trimmed = hex.trim();
  if (!trimmed.startsWith("#")) {
    return null;
  }

  const body = trimmed.slice(1);
  if (/^[0-9A-Fa-f]{3}$/.test(body)) {
    return `#${body[0]}${body[0]}${body[1]}${body[1]}${body[2]}${body[2]}`.toUpperCase();
  }

  if (/^[0-9A-Fa-f]{6}$/.test(body)) {
    return `#${body.toUpperCase()}`;
  }

  return null;
}

function extractBrandColors(svg) {
  const fills = [...svg.matchAll(/fill="([^"]+)"/g)]
    .map((match) => match[1].trim())
    .filter(
      (fill) =>
        !fill.startsWith("url(") &&
        !SKIP_FILLS.has(fill.toLowerCase()) &&
        normalizeHex(fill),
    )
    .map((fill) => normalizeHex(fill))
    .filter(Boolean);

  const primary = fills[0] ?? null;
  const secondary = fills.find((fill, index) => index > 0 && fill !== primary) ?? null;

  return primary ? { primary, ...(secondary ? { secondary } : {}) } : null;
}

function buildCatalogEntries(logos) {
  const banks = [];
  const ewallets = [];
  const international = [];

  for (const logo of logos) {
    const entry = { name: logo.name, slug: logo.slug };

    if (logo.category === "bank-logo") {
      banks.push(entry);
      continue;
    }

    if (logo.category === "e-wallet") {
      ewallets.push(entry);
      continue;
    }

    if (logo.category === "remittance" || EXTRA_SLUGS.has(logo.slug)) {
      international.push(entry);
    }
  }

  const sortByName = (a, b) => a.name.localeCompare(b.name, "en");

  return {
    banks: banks.sort(sortByName),
    ewallets: ewallets.sort(sortByName),
    international: international.sort(sortByName),
  };
}

function renderCatalogFile({ banks, ewallets, international }) {
  return `/** AUTO-GENERATED by \`npm run fintech:logos\` — do not edit. */
import type { WalletInstitutionEntry } from "@/config/wallet-institutions";

export const GENERATED_BANK_INSTITUTIONS: WalletInstitutionEntry[] = ${JSON.stringify(banks, null, 2)};

export const GENERATED_EWALLET_INSTITUTIONS: WalletInstitutionEntry[] = ${JSON.stringify(ewallets, null, 2)};

export const GENERATED_INTERNATIONAL_INSTITUTIONS: WalletInstitutionEntry[] = ${JSON.stringify(international, null, 2)};

export const GENERATED_FINTECH_LOGO_SLUGS: string[] = ${JSON.stringify(
    [...banks, ...ewallets, ...international].map((entry) => entry.slug),
    null,
    2,
  )};
`;
}

function renderBrandColorsFile(brandColors) {
  const entries = Object.entries(brandColors).sort(([a], [b]) => a.localeCompare(b));

  return `/** AUTO-GENERATED by \`npm run fintech:logos\` — do not edit. */
import type { WalletInstitutionBrandColor } from "@/config/wallet-institutions";

export const GENERATED_WALLET_INSTITUTION_BRAND_COLORS: Record<
  string,
  WalletInstitutionBrandColor
> = ${JSON.stringify(Object.fromEntries(entries), null, 2)};
`;
}

async function syncFintechLogos() {
  const manifestResponse = await fetch(MANIFEST_URL);
  if (!manifestResponse.ok) {
    throw new Error(`Failed to fetch manifest: ${manifestResponse.status}`);
  }

  const manifest = await manifestResponse.json();
  const selectedLogos = manifest.logos.filter((logo) => {
    if (isVariantSlug(logo.slug)) {
      return false;
    }

    return SYNC_CATEGORIES.has(logo.category) || EXTRA_SLUGS.has(logo.slug);
  });

  const catalog = buildCatalogEntries(selectedLogos);
  const allEntries = [
    ...catalog.banks,
    ...catalog.ewallets,
    ...catalog.international,
  ];

  await mkdir(OUTPUT_DIR, { recursive: true });

  let saved = 0;
  let failed = 0;
  const brandColors = {};

  for (const entry of allEntries) {
    const response = await fetch(`${CDN_BASE}/${entry.slug}.svg`);

    if (!response.ok) {
      console.error(`Failed ${entry.slug}: ${response.status}`);
      failed += 1;
      continue;
    }

    const svg = await response.text();
    await writeFile(path.join(OUTPUT_DIR, `${entry.slug}.svg`), svg, "utf8");

    const colors = extractBrandColors(svg);
    if (colors) {
      brandColors[entry.slug] = colors;
    }

    saved += 1;
  }

  await writeFile(CATALOG_OUTPUT, renderCatalogFile(catalog), "utf8");
  await writeFile(BRAND_COLORS_OUTPUT, renderBrandColorsFile(brandColors), "utf8");

  console.log(
    `Synced ${saved} logos to public/icons/fintech (${failed} failed).`,
  );
  console.log(
    `Catalog: ${catalog.banks.length} banks, ${catalog.ewallets.length} e-wallets, ${catalog.international.length} international.`,
  );
  console.log(`Generated ${Object.keys(brandColors).length} brand color entries.`);
}

await syncFintechLogos();
